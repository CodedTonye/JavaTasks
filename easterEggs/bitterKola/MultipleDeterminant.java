public class MultipleDeterminant {
  
    public static void main(String[] args) {

	int num1 = 8;
	int num2 = 4;
	
	if (num1 % num2 == 0) {
	    System.out.println("The First number is a multiple of the second.");
	} else {
	    System.out.println("The First number is not a multiple of the second.");
	}
  }
}